/**
 * @param {string} template
 * @param {Record<string, string|number|null|undefined>} vars
 */
function fillTemplate(template, vars) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => {
    const value = vars[key];
    return value == null ? "" : String(value);
  });
}

