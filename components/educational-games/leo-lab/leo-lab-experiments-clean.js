/** @typedef {'easy' | 'medium' | 'hard'} DifficultyId */

import {
  EXPERIMENT_SHELF_PRESET,
  SHELF_PRESETS,
} from "./leo-lab-shelf-presets.js";

/**
 * @param {{
 *   id: string,
 *   difficulty: DifficultyId,
 *   title: string,
 *   prompt: string,
 *   missionIcon: string,
 *   pickCount: number,
 *   validItems: string[],
 *   exactMatch?: boolean,
 *   fact: string,
 *   resultIcon?: string,
 *   shelfItems?: string[],
 *   shelfPreset?: string,
 * }} cfg
 */
function labExp(cfg) {
  const presetKey = cfg.shelfPreset ?? EXPERIMENT_SHELF_PRESET[cfg.id];
  const shelfItems = cfg.shelfItems ?? (presetKey ? SHELF_PRESETS[presetKey] : undefined);
  if (!shelfItems?.length) {
    throw new Error(`missing shelfItems for experiment: ${cfg.id}`);
  }
  return {
    ...cfg,
    shelfItems: [...shelfItems],
    exactMatch: cfg.exactMatch ?? false,
    resultText: "Great job!",
    resultIcon: cfg.resultIcon ?? cfg.missionIcon,
  };
}

