/** Build storage path for reporting */
export function storagePathFor(card) {
  if (card.card_type === "event") return `events/${card.card_key}.webp`;
  const prefix = card.card_type === "shop" ? "shop" : "achievements";
  return `${prefix}/${card.series_slug}/${card.card_key}.webp`;
}
