function buildPage(pageId) {
  const meta = MATH_G6_PAGE_META[pageId];
  const sections = PAGE_SECTIONS[pageId];
  if (!meta || !sections || sections.length !== 7) {
    throw new Error(`Missing content for ${pageId}`);
  }

  const sectionBlocks = sections
    .map((body, i) => `## ${i + 1}. ${SECTION_TITLES[i]}\n\n${body}`)
    .join("\n\n---\n\n");

  return `# ${meta.titleHe}

## Metadata

| Field | Value |
|-------|-------|
| **learning_page_id** | \`math:g6:${pageId}\` |
| **skill_id** | \`${meta.skillId}\` |
| **subject** | math |
| **grade** | g6 |
| **age_band** | grades_5_6 |
| **page_type** | ${meta.pageType} |
| **approval_status** | draft |
| **title_hebrew** | ${meta.titleHe} \`[DRAFT — not owner-approved]\` |

**Source references:**
- \`data/curriculum-spine/v1/skills.json\`
- \`docs/learning-book/MATH_GRADE_6_LEARNING_BOOK_PLAN.md\`
- \`docs/learning-book/MATH_LEARNING_PAGE_TEMPLATE.md\`

**Content scope:** ${meta.scope}

---

${sectionBlocks}
`;
}

fs.mkdirSync(OUT_DIR, { recursive: true });

for (const pageId of MATH_G6_PAGE_ORDER) {
  const outPath = path.join(OUT_DIR, `${pageId}.md`);
  fs.writeFileSync(outPath, buildPage(pageId), "utf8");
  console.log(`Wrote ${pageId}.md`);
}

