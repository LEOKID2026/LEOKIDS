function walkPageTxt(dir) {
  /** @type {string[]} */
  const out = [];
  for (const name of fs.readdirSync(dir)) {
    const p = path.join(dir, name);
    if (fs.statSync(p).isDirectory()) out.push(...walkPageTxt(p));
    else if (name.endsWith(".txt") && /page-\d+\.txt$/.test(name)) out.push(p);
  }
  return out;
}

