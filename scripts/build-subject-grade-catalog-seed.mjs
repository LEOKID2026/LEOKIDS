#!/usr/bin/env node
/**
 * Build subject-grade-defaults artifact + SQL seed for migration 097.
 * Run: node scripts/build-subject-grade-catalog-seed.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { buildSubjectGradeDefaultsArtifact } from "../lib/learning/subject-permissions/subject-grade-defaults.resolver.js";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");

const artifact = buildSubjectGradeDefaultsArtifact();

if (artifact.hasUnresolvedConflicts) {
  console.error("Unresolved conflicts — stop build:");
  for (const c of artifact.conflicts) {
    console.error(`- [${c.subjectKey || "?"}] ${c.type}: ${c.message}`);
    if (c.primary) console.error(`  primary: ${c.primary.join(", ")}`);
    if (c.secondary) console.error(`  secondary: ${c.secondary.join(", ")}`);
  }
  process.exit(1);
}

const artifactPath = path.join(root, "data/subject-permissions/subject-grade-defaults.matrix.json");
fs.mkdirSync(path.dirname(artifactPath), { recursive: true });
fs.writeFileSync(artifactPath, `${JSON.stringify(artifact, null, 2)}\n`, "utf8");

const subjectRows = artifact.subjects
  .map(
    (s) =>
      `  ('${s.subject_key}', '${s.display_name_he.replace(/'/g, "''")}', ${s.sort_order}, ${s.is_active})`
  )
  .join(",\n");

const matrixRows = [];
for (const [gradeKey, subjects] of Object.entries(artifact.matrix)) {
  for (const [subjectKey, cell] of Object.entries(subjects)) {
    matrixRows.push(
      `  ('${gradeKey}', '${subjectKey}', ${cell.is_grade_suitable}, ${cell.is_enabled_by_default})`
    );
  }
}

const sql = `-- AUTO-GENERATED by scripts/build-subject-grade-catalog-seed.mjs — do not edit by hand
-- Generated: ${artifact.generatedAt}

insert into public.subject_permission_catalog (subject_key, display_name_he, sort_order, is_active)
values
${subjectRows}
on conflict (subject_key) do update set
  display_name_he = excluded.display_name_he,
  sort_order = excluded.sort_order,
  is_active = excluded.is_active;

insert into public.subject_grade_default_catalog (grade_key, subject_key, is_grade_suitable, is_enabled_by_default)
values
${matrixRows.join(",\n")}
on conflict (grade_key, subject_key) do update set
  is_grade_suitable = excluded.is_grade_suitable,
  is_enabled_by_default = excluded.is_enabled_by_default;
`;

const sqlPath = path.join(root, "data/subject-permissions/subject-grade-catalog-seed.sql");
fs.writeFileSync(sqlPath, sql, "utf8");

console.log(`Wrote artifact: ${artifactPath}`);
console.log(`Wrote seed SQL: ${sqlPath}`);
console.log(`Subjects: ${artifact.subjects.length}, matrix rows: ${matrixRows.length}`);
