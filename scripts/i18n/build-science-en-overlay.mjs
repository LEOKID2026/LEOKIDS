#!/usr/bin/env node
/**
 * Build id-keyed English overlay for science MCQ bank.
 * Run: node scripts/i18n/build-science-en-overlay.mjs
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { pathToFileURL } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "../..");
const mod = (rel) => import(pathToFileURL(join(ROOT, rel)).href);

const HEBREW_RE = /[\u0590-\u05FF]/;

async function main() {
  const { SCIENCE_QUESTIONS_HE } = await mod("data/science-questions.js");
  const { translateScienceFields } = await mod("utils/learning-content-en/science-translate.js");

  const overlay = {};
  let remaining = 0;

  for (const row of SCIENCE_QUESTIONS_HE) {
    const en = translateScienceFields(row);
    overlay[row.id] = {
      stem: en.stem,
      options: en.options,
      explanation: en.explanation,
      theoryLines: en.theoryLines,
    };
    const blob = [
      en.stem,
      ...(en.options || []),
      en.explanation,
      ...(en.theoryLines || []),
    ].join(" ");
    if (HEBREW_RE.test(blob)) remaining += 1;
  }

  const outPath = join(ROOT, "data/science-questions-en-overlay.js");
  writeFileSync(
    outPath,
    `/** Auto-generated by scripts/i18n/build-science-en-overlay.mjs — do not hand-edit. */\nexport const SCIENCE_EN_OVERLAY = ${JSON.stringify(overlay, null, 2)};\n`,
    "utf8"
  );

  console.log(
    `[science-en-overlay] wrote ${Object.keys(overlay).length} entries; ${remaining} rows still contain Hebrew`
  );
  if (remaining > 0) process.exitCode = 0;
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
